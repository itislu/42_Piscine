#include <unistd.h>

int	main(int argc, char **argv)
{
	int	a;
	int	c;

	a = 1;
	while (a < argc)
	{
		c = 0;
		while (argv[a][c])
		{
			write(1, &argv[a][c], 1);
			c++;
		}
		write(1, "\n", 1);
		a++;
	}
}
